    public void fillTable() {
        DefaultTableModel tbModel = (DefaultTableModel) tbStudent.getModel();
        tbModel.setRowCount(0);//Xoa data trong table
        for (Student st : stDao.findStudentByName(strFind)) {
            Object dataRow[] = new Object[6];
            dataRow[0] = st.getMaSv();
            dataRow[1] = st.getHoTen();
            dataRow[2] = st.getEmail();
            dataRow[3] = st.getSoDT();
            dataRow[4] = st.isGioiTinh() ? "Nam" : "Nu";
            dataRow[5] = st.getDiaChi();
            tbModel.addRow(dataRow);
        }
    }

