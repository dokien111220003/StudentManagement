    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Student st = getModel();
        if (stDao.findStudentByID(txtID.getText()) != null) {
            JOptionPane.showMessageDialog(this, "Ma sinh vien nay da ton tai \n Hay su dung ma sinh vien khac");
        } else {
            if (stDao.add(st) > 0) {
                fillTable();
                lblStatus.setText("Them thanh cong" + stDao.getAll().size() + " of" + stDao.getAll().size());
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Student st = getModel();
        if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ban chua nhap ma Sinh Vien");
        } else {
            if (stDao.update(st) > 0) {
                fillTable();
                lblStatus.setText("Sua thanh cong" + stDao.getAll().size() + " of" + stDao.getAll().size());
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:

        if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ban chua nhap ma Sinh Vien");
        } else {
            if (stDao.delete(txtID.getText()) > 0) {
                fillTable();
                lblStatus.setText("Xoa thanh cong" + stDao.getAll().size() + " of" + stDao.getAll().size());
            }
        }
    }//GEN-LAST:event_btnDelActionPerformed

    private void btnFirstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFirstActionPerformed
        // TODO add your handling code here:
        posCurrent = 0;
        Student st = stDao.getAll().get(posCurrent);
        setModel(st);
        lblStatus.setText((posCurrent + 1) + " of" + stDao.getAll().size());
    }//GEN-LAST:event_btnFirstActionPerformed

    private void btnLastActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLastActionPerformed
        // TODO add your handling code here:
        posCurrent = stDao.getAll().size() - 1;
        Student st = stDao.getAll().get(posCurrent);
        setModel(st);
        lblStatus.setText((posCurrent + 1) + " of" + stDao.getAll().size());
    }//GEN-LAST:event_btnLastActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        posCurrent = posCurrent - 1;
        if (posCurrent <= 0) {
            posCurrent = 0;
        }
        Student st = stDao.getAll().get(posCurrent);
        setModel(st);
        lblStatus.setText((posCurrent + 1) + " of" + stDao.getAll().size());
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        posCurrent = posCurrent + 1;
        if (posCurrent >= stDao.getAll().size() - 1) {
            posCurrent = stDao.getAll().size() - 1;
        }
        Student st = stDao.getAll().get(posCurrent);
        setModel(st);
        lblStatus.setText((posCurrent + 1) + " of" + stDao.getAll().size());
    }//GEN-LAST:event_btnNextActionPerformed

