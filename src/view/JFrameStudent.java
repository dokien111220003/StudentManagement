    public Student getModel() {
        Student st = null;
        StringBuilder sb = new StringBuilder();//tao chuoi co the thay doi
        Validator.isEmpty(txtID, sb, "Ban chua nhap ma sinh vien");
        Validator.isEmpty(txtName, sb, "Ban chua nhap ten sinh vien");
        Validator.isEmpty(txtEmail, sb, "Ban chua nhap email sinh vien");
        Validator.isEmpty(txtPhone, sb, "Ban chua nhap so dien thoai");
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString());
        } else {
            st = new Student();//st = rong
            st.setMaSv(txtID.getText().trim());
            st.setHoTen(txtName.getText().trim());
            st.setMaSv(txtID.getText().trim());
            st.setEmail(txtEmail.getText().trim());
            st.setSoDT(txtPhone.getText().trim());
            st.setDiaChi(txtAddress.getText().trim());
            boolean gt = false;
            if (rdMale.isSelected()) {
                gt = true;
            }
            st.setGioiTinh(gt);
        }

        return st;
    }

    public void setModel(Student st) {
        txtID.setText(st.getMaSv());
        txtName.setText(st.getHoTen());
        txtEmail.setText(st.getEmail());
        txtPhone.setText(st.getSoDT());
        txtAddress.setText(st.getDiaChi());
        if (st.isGioiTinh()) {
            rdMale.isSelected();
        } else {
            rdFemale.isSelected();
        }
    }

